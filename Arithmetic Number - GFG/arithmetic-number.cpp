class Solution{
public:
    int inSequence(int A, int B, int C){
        if(C==0)
        return A==B;
        
        int n1 = (B-A)/C + 1;
        int b = A + (n1-1)*C;
        if(n1<=0)
        return 0;
        return B==b;
    }
};

